package com.simple.chris.pebble

class GradientCharacteristics (gradientName:String, gradientStartColour:Int, gradientEndColour:Int) {

    var gradientName:String
    var gradientStartColour:Int
    var gradientEndColour:Int

    init {
        this.gradientName = gradientName
        this.gradientStartColour = gradientStartColour
        this.gradientEndColour = gradientEndColour
    }

}